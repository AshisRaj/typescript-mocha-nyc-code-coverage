/**
 * The sum function takes the two numbers, a and b, and adds them together, returning the sum as an integer. 
 * @param a 
 * @param b 
 * @returns 
 */
export function sum(a: number, b: number): number {
    return a + b;
}

/**
 * The subtract function takes the two numbers, a and b, and subtracts the second number from the first, returning the result as an integer. 
 * @param a 
 * @param b 
 * @returns 
 */
export function subtract(a: number, b: number): number {
    return a - b;
}

/**
 * The multiply function takes the two numbers, a and b, and multiplies them together, returning the product as an integer. 
 * @param a 
 * @param b 
 * @returns 
 */
export function multiply(a: number, b: number): number {
    return a * b;
}

/**
 * The divide function takes the two numbers, a and b, and divides the first number by the second, returning the quotient as an integer. 
 * @param a 
 * @param b 
 * @returns 
 */
export function divide(a: number, b: number): number {
    return a / b;
}
